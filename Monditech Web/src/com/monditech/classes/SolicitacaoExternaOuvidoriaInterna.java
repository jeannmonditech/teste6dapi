package com.monditech.classes;

public class SolicitacaoExternaOuvidoriaInterna {
	
	private String unidade;
	private String setor;
	private String turno;
	private String solicitante;
	private String tratamento;
	private String assunto;
	private String outroAssunto;
	private String comentarios;
	
	
	public String getUnidade() {
		return unidade;
	}
	public void setUnidade(String unidade) {
		this.unidade = unidade;
	}
	public String getSetor() {
		return setor;
	}
	public void setSetor(String setor) {
		this.setor = setor;
	}
	public String getTurno() {
		return turno;
	}
	public void setTurno(String turno) {
		this.turno = turno;
	}
	public String getSolicitante() {
		return solicitante;
	}
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	public String getTratamento() {
		return tratamento;
	}
	public void setTratamento(String tratamento) {
		this.tratamento = tratamento;
	}
	public String getAssunto() {
		return assunto;
	}
	public void setAssunto(String assunto) {
		this.assunto = assunto;
	}
	public String getOutroAssunto() {
		return outroAssunto;
	}
	public void setOutroAssunto(String outroAssunto) {
		this.outroAssunto = outroAssunto;
	}
	public String getComentarios() {
		return comentarios;
	}
	public void setComentarios(String comentarios) {
		this.comentarios = comentarios;
	}
}
